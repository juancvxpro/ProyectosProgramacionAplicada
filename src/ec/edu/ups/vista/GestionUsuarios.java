    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int d = JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar el usuario " + txtCorreo.getText().trim());
        if (d == JOptionPane.YES_OPTION) {
            Usuario comparar = new   Usuario(txtGcedula.getText().trim(),null,null,null,null,null,null,null);
            comparar.setCodigo(id);
            
            Optional<Usuario> u = controladorUsuario.buscar(comparar);
            Usuario usu = u.get();
            System.out.println("" + usu);
           

            if ( controladorUsuario.eliminar(usu)) {
                try {
              
                    controladorUsuario.guardarDatos("datos/Usuario.obj");
                } catch (IOException ex) {
                    Logger.getLogger(GestionUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, "eliminado exitosamente");
                cargarUsuariosTbl();
            } else if (d == JOptionPane.NO_OPTION) {
                cargarUsuariosTbl();
            }

        }

    }//GEN-LAST:event_btnEliminarActionPerformed

