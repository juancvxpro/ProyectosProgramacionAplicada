    public void cargarUsuariosTbl(){
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setRowCount(0);
        if (controladorUsuario.usuarios() != null) {
            for (Usuario usuario : controladorUsuario.usuarios()) {
                Object[] rowData = {usuario.getCodigo(),usuario.getCedula(),usuario.getNombre(),usuario.getApellido(),usuario.getDireccion(),usuario.getTlf(),usuario.getCorreo(),usuario.getPass()};
                modelo.addRow(rowData);
                tblUsuarios.setModel(modelo);
            }
        } else {
            System.out.println("LISTA VACIA");
        }
    
    }
