    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        String pass = "";
        char[] pass1 = jGpr.getPassword();
        for (int i = 0; i < pass1.length; i++) {
            pass = pass + pass1[i];
        }
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        Date fechaN = new Date();
        try {
            fechaN = formato.parse(txtFechaN.getText().trim());
        } catch (ParseException ex) {
            System.out.println(ex);
        }

        //txtCargo.getText().trim(),txtCorreo.getText().trim(),txtGcedula.getText().trim(),txtGnombre.getText().trim(),txtGapellido.getText().trim(),txtDireccion.getText().trim(),cbxGenero.getSelectedItem().toString().trim(),fechaN,txtEstadoCivil.getText().trim(),"Ciudadano"
        //String cargo, String correo, String pass, String cedula, String nombres, String apellidos, String direccion, String genero, Date fechaNacimiento, String estadoCivil, String rol
        Autoridad usuario = new Autoridad(txtGcedula.getText().trim(), txtGnombre.getText().trim(), txtGapellido.getText().trim(),
                txtDireccion.getText().trim(), cbxGenero.getSelectedItem().toString().trim(),
                fechaN, txtEstadoCivil.getText().trim(), "Autoridad", txtCargo.getText().trim(),
                txtCorreo.getText().trim(), pass.trim());

        System.out.println(usuario);
        if (controlador.validar(usuario)) {
            controlador.crear(usuario);
            JOptionPane.showMessageDialog(this, "Usuario creado correctamente");
            Limpiar();
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(this, "La cedula ingresada no es valida");
            Limpiar();
        }
        ;
        try {
            controlador.guardarDatos("datos/Autoridad.obj");
        } catch (IOException ex) {
            Logger.getLogger(RegistrarUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnGuardar1ActionPerformed

