    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String pass = "";
        char[] pass1 = jGpr.getPassword();
        for (int i = 0; i < pass1.length; i++) {
            pass = pass + pass1[i];
        }
        Usuario usuario = new Usuario(txtGcedula.getText(), txtGnombre.getText(), txtGapellido.getText(), txtGcorreo.getText(), pass);
        if (controlador.validar(usuario)) {
            controlador.crear(usuario);
            JOptionPane.showMessageDialog(this, "Usuario creado correctamente");
            Limpiar();
            this.dispose();

        }else{
         JOptionPane.showMessageDialog(this, "La cedula ingresada no existe");
          Limpiar();
        }
        ;


    }//GEN-LAST:event_btnGuardarActionPerformed

