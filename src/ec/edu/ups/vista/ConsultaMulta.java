    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        if (txtConsulta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "CAMPO VACIO");
        } else {
            String r = controladorEspacios.buscarEspacioAsignado(Integer.parseInt(txtConsulta.getText().trim()));

            if (r.equals("")) {
                JOptionPane.showMessageDialog(this, "El espacio solicitado no cuenta con alg√∫n tipo de servicio de arrendamiento o multa");
            } else {
                int id = Integer.parseInt(r);

                ClienteFijo comparar = new ClienteFijo(id, null, null, null, null, 0, null, null, null, null, null);

                TicketClienteMomentaneo comp = new TicketClienteMomentaneo();

                comp.setId(id);

                Optional<ClienteFijo> a = controladorClienteFijo.buscar(comparar);

                Optional<TicketClienteMomentaneo> b = controladorTicketCliente.buscar(comp);

                ClienteFijo c = a.get();

                TicketClienteMomentaneo d = b.get();

                if (c != null) {
                    JOptionPane.showMessageDialog(this, "El espacio solicitado esta ocupado por " + c.getNombre() + " " + c.getApellido());
                    if (c.getMulta() > 0) {
                        JOptionPane.showMessageDialog(this, "Tiene una multa de " + c.getMulta());
                    }
                } else if (d != null) {

                    JOptionPane.showMessageDialog(this, "El espacio solicitado esta ocupado por " + d.getNombre() + " ");
                    if (c.getMulta() > 0) {
                        JOptionPane.showMessageDialog(this, "Tiene una multa de " + d.getMulta());
                    }
                }

            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

